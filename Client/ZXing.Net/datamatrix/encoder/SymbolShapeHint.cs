namespace ZXing.Datamatrix.Encoder
{
    /// <summary>
    ///     Enumeration for DataMatrix symbol shape hint. It can be used to force square or rectangular
    ///     symbols.
    /// </summary>
    public enum SymbolShapeHint
    {
        FORCE_NONE,
        FORCE_SQUARE,
        FORCE_RECTANGLE,
    }
}
